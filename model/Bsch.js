const mongoose = require("mongoose");
const BschSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
});

const BschModel = mongoose.model("Users", BschSchema);
module.exports = BschModel;
